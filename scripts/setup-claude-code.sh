#!/usr/bin/env bash
set -euo pipefail

repo_root="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
claude_skills_dir="$repo_root/.claude/skills"
generated_marker="Generated by scripts/setup-claude-code.sh. Do not edit."

source "$repo_root/scripts/agent-link-utils.sh"

create_claude_instruction_file() {
  local agents_path="$1"
  local agents_dir
  local claude_path

  agents_dir="$(dirname "$agents_path")"

  if [ "$agents_dir" = "." ]; then
    claude_path="$repo_root/CLAUDE.md"
  else
    claude_path="$repo_root/$agents_dir/CLAUDE.md"
  fi

  if [ -L "$claude_path" ]; then
    if [ "$(readlink "$claude_path")" = "AGENTS.md" ]; then
      echo "Keeping Claude instruction file $claude_path"
      return
    fi

    rm "$claude_path"
  elif [ -e "$claude_path" ]; then
    if [ "$(sed -n '1p' "$claude_path")" = "<!-- $generated_marker -->" ]; then
      rm "$claude_path"
    else
      echo "Skipping Claude memory $claude_path: file already exists and is not generated" >&2
      return
    fi
  fi

  if ln -s AGENTS.md "$claude_path" 2>/dev/null; then
    echo "Linked Claude instruction file $claude_path"
    return
  fi

  {
    printf '<!-- %s -->\n' "$generated_marker"
    printf '@AGENTS.md\n'
  } >"$claude_path"
  echo "Wrote Claude instruction import $claude_path"
}

prune_stale_claude_instruction_files() {
  git -C "$repo_root" ls-files --others --ignored --exclude-standard -z -- 'CLAUDE.md' '*/CLAUDE.md' |
  while IFS= read -r -d '' claude_path; do
    local full_claude_path
    local agents_path

    full_claude_path="$repo_root/$claude_path"
    agents_path="$(dirname "$claude_path")/AGENTS.md"

    if [ "$(dirname "$claude_path")" = "." ]; then
      agents_path="AGENTS.md"
    fi

    if git -C "$repo_root" ls-files --error-unmatch "$agents_path" >/dev/null 2>&1; then
      continue
    fi

    if [ -L "$full_claude_path" ] && [ "$(readlink "$full_claude_path")" = "AGENTS.md" ]; then
      rm "$full_claude_path"
      echo "Pruned stale Claude instruction file $full_claude_path"
      continue
    fi

    if [ -f "$full_claude_path" ] && [ "$(sed -n '1p' "$full_claude_path")" = "<!-- $generated_marker -->" ]; then
      rm "$full_claude_path"
      echo "Pruned stale Claude instruction file $full_claude_path"
    fi
  done
}

sync_claude_instruction_files() {
  prune_stale_claude_instruction_files

  git -C "$repo_root" ls-files -z -- 'AGENTS.md' '*/AGENTS.md' |
  while IFS= read -r -d '' agents_path; do
    create_claude_instruction_file "$agents_path"
  done
}

sync_skill_links_from_cursor "$repo_root" "$claude_skills_dir" "Claude"
sync_claude_instruction_files

echo "Done. Restart Claude Code to pick up generated skills and memory files."
